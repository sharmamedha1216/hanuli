package operations;

public class basicOperations {

	void toAdd()
	{
		int lhs = 3;
		int rhs = 9;
		int result;
		result = lhs + rhs;
		System.out.println("In Add:"+result);
	}
	
	void toSub() {
		int lhs=1;
		int rhs =1;
		int result;
		result = lhs-rhs;
		System.out.println("In Sub:"+result);
	}
	
}
